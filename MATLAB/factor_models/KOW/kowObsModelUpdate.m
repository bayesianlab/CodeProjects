function [ draw,themean,Hessian ] = kowObsModelUpdate(guess, precision, StatePrecision, oldMean,...
    oldHessian, Restricted, PriorPre, logdetPriorPre, ObsPriorMean,ObsPriorVar, factor, yt, i, burnin)
MaxRecursionDepth = 1;
if i < burnin
    options = optimoptions(@fminunc, 'Algorithm', 'quasi-newton',...
        'Display', 'off', 'FiniteDifferenceType', 'forward',...
        'OptimalityTolerance', 1e-8, 'MaxIterations', 100);
else
    options =  optimoptions(@fminunc, 'Algorithm', 'quasi-newton',...
        'Display', 'off', 'FiniteDifferenceType', 'forward',...
        'OptimalityTolerance', 1e-8);
end
[themean,Hessian] = kowOptimize(guess, StatePrecision,  precision,oldMean,...
    oldHessian, options, MaxRecursionDepth,ObsPriorMean,ObsPriorVar, factor, yt);
if Restricted == 1
    draw = kowMHR(guess,themean,Hessian,yt(:),StatePrecision,precision,...
        PriorPre,logdetPriorPre);
else
    draw = kowMHUnRes(guess,themean,Hessian,yt(:),StatePrecision,precision,PriorPre,...
        logdetPriorPre);
end
end

