function [out] = log_f_gTsup(x, alpha, beta, mu, sigmaSqd, sup)
if sup < 1e-8
 out = log(gamma(alpha)/ ((beta^alpha)*sqrt(2*pi*sigmaSqd))) + ...
    (1 - alpha) * log(x) + ...
    (x*beta) - ((1/(2*sigmaSqd))* (x - mu)^2) ;
else
    out = log(gamma(alpha)/ (sup*(beta^alpha)*sqrt(2*pi*sigmaSqd))) + ...
    (1 - alpha) * log(x) + ...
    (x*beta) - ((1/(2*sigmaSqd))* (x - mu)^2) ;
end
end

